#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		ls;
	int		i;

	i = 0;
	if (!s || !f)
		return (NULL);
	ls = ft_strlen(s);
	str = malloc(ls + 1);
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
