#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t		i;
	size_t		j;
	size_t		ls;

	i = 0;
	j = 0;
	ls = ft_strlen((char *)src);
	if (dstsize == 0)
		return (ls);
	while (src[i] && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ls);
}
